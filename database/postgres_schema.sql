DROP VIEW IF EXISTS vw_painel_servicos;
DROP TABLE IF EXISTS movimentacoes;
DROP TABLE IF EXISTS servicos_juridicos;
DROP TABLE IF EXISTS clientes;
DROP TABLE IF EXISTS responsaveis;

CREATE TABLE responsaveis (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(160) NOT NULL,
    oab VARCHAR(30),
    email VARCHAR(160),
    telefone VARCHAR(30),
    ativo BOOLEAN NOT NULL DEFAULT TRUE,
    criado_em TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE clientes (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nome VARCHAR(180) NOT NULL,
    cpf VARCHAR(20) NOT NULL,
    telefone VARCHAR(30) NOT NULL,
    email VARCHAR(160) NOT NULL,
    observacoes TEXT,
    criado_em TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    atualizado_em TIMESTAMP(0),
    CONSTRAINT uq_clientes_cpf UNIQUE (cpf)
);

CREATE TABLE servicos_juridicos (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    cliente_id INTEGER NOT NULL REFERENCES clientes(id),
    responsavel_id INTEGER REFERENCES responsaveis(id),
    tipo VARCHAR(40) NOT NULL,
    status VARCHAR(30) NOT NULL DEFAULT 'Ativo',
    honorarios NUMERIC(12,2) NOT NULL DEFAULT 0,
    data_abertura DATE NOT NULL DEFAULT CURRENT_DATE,
    proximo_passo VARCHAR(500) NOT NULL,
    observacoes TEXT,
    criado_em TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP,
    atualizado_em TIMESTAMP(0),
    CONSTRAINT ck_servicos_tipo CHECK (tipo IN ('Consultoria', 'Processo', 'Contrato', 'Audiencia', 'Planejamento')),
    CONSTRAINT ck_servicos_status CHECK (status IN ('Ativo', 'Aguardando', 'Concluido')),
    CONSTRAINT ck_servicos_honorarios CHECK (honorarios >= 0)
);

CREATE TABLE movimentacoes (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    servico_id INTEGER NOT NULL REFERENCES servicos_juridicos(id) ON DELETE CASCADE,
    titulo VARCHAR(160) NOT NULL,
    descricao TEXT NOT NULL,
    data_movimentacao TIMESTAMP(0) NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX ix_clientes_nome ON clientes(nome);
CREATE INDEX ix_servicos_status ON servicos_juridicos(status);
CREATE INDEX ix_servicos_tipo ON servicos_juridicos(tipo);
CREATE INDEX ix_servicos_cliente_id ON servicos_juridicos(cliente_id);

