CREATE VIEW vw_painel_servicos AS
SELECT
    s.id AS servico_id,
    c.id AS cliente_id,
    c.nome AS cliente,
    c.cpf,
    c.telefone,
    c.email,
    s.tipo,
    s.status,
    s.honorarios,
    s.data_abertura,
    r.nome AS responsavel,
    s.proximo_passo,
    s.observacoes
FROM servicos_juridicos s
INNER JOIN clientes c ON c.id = s.cliente_id
LEFT JOIN responsaveis r ON r.id = s.responsavel_id;

